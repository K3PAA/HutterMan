class Breakable {
  static width = 32
  static height = 32
  constructor({ position }) {
    this.position = position
    this.width = 32
    this.height = 32
  }

  draw() {
    c.drawImage(box, this.position.x, this.position.y, this.width, this.height)
  }
}

const breakable2D = []

const bricks = []
for (let i = 0; i < breakable.length; i += 32) {
  breakable2D.push(breakable.slice(i, i + 32))
}

breakable2D.forEach((row, y) => {
  row.forEach((line, x) => {
    if (line != 0) {
      bricks.push(
        new Breakable({
          position: {
            x: x * Breakable.width,
            y: y * Breakable.height,
          },
        })
      )
    }
  })
})

export default bricks
